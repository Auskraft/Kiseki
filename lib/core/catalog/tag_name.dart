/// Нормализация имени тега для UNIQUE-инварианта и поиска (TECH_DESIGN §4.5).
/// Вычисляется в ОДНОМ месте (репозиторий тегов), не в UI.
///
/// TODO(beta): добавить полноценную NFC-нормализацию (пакет `unorm_dart`),
/// чтобы прекомпозированные/декомпозированные формы юникода не плодили
/// визуально одинаковые дубли. Сейчас — lower + trim + схлопывание пробелов.
String normalizeTagName(String name) =>
    name.trim().toLowerCase().replaceAll(RegExp(r'\s+'), ' ');
